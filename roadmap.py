def get_today_focus(py_weekday: int):
    """با گرفتن weekday پایتون (Mon=0)، تسک‌های امروز را برمی‌گرداند."""
    fa_idx = python_weekday_to_fa_index(py_weekday)
    focus = DAILY_FOCUS[fa_idx]
    tasks = list(focus["tasks"]) + list(DAILY_ALWAYS)
    return {
        "weekday_name": WEEKDAYS_FA[fa_idx],
        "focus_title": focus["title"],
        "tasks": tasks,
    }


def get_month_goals(month_number: int):
    """month_number از ۱ تا ۱۲."""
    for m in MONTHLY_ROADMAP:
        if m["month"] == month_number:
            return m
    return MONTHLY_ROADMAP[-1]
