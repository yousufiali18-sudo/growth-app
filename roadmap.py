# نگاشت weekday پایتون (دوشنبه=0 ... یکشنبه=6) به ایندکس هفتهٔ ایرانی (شنبه=0)
def python_weekday_to_fa_index(py_weekday: int) -> int:
    # py: Mon=0,Tue=1,Wed=2,Thu=3,Fri=4,Sat=5,Sun=6
    mapping = {5: 0, 6: 1, 0: 2, 1: 3, 2: 4, 3: 5, 4: 6}
    return mapping[py_weekday]


